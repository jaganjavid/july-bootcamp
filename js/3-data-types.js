

// Primitive type


// String
const name = "Jagan";

console.log(name);
console.log(typeof name);

// Number

const age = 27;

console.log(age)
console.log(typeof age)

// Boolean

const hadDinner = true;

console.log(hadDinner);
console.log(typeof hadDinner);

// Null

const box = null;

console.log(box);
console.log(typeof box);

// Undefined

let x;

console.log(x);
console.log(typeof x);



// Reference Types

const array = [1,"Hello", true];

console.log(array);
console.log(typeof array);

const car = {
    // KEY and value pairs
    name:"BMW"
}

console.log(car);
console.log(typeof car);